#include<iostream>
#include<algorithm>
#include<string>
using namespace std;

long long dp[101][3][3][2];//위치,자음,모음,L

int main() {
	ios::sync_with_stdio(false); cin.tie(0); cout.tie(0);

	string s; cin >> s;
	for (int i = 1; i <= s.size(); i++) {
		char c = s[i - 1];
		if (c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U') {
			if (i == 1) {
				dp[1][0][1][0] = 1;
			}
			else {
				dp[i][0][1][0] = dp[i - 1][1][0][0] + dp[i - 1][2][0][0];
				dp[i][0][1][1] = dp[i - 1][1][0][1] + dp[i - 1][2][0][1];
				dp[i][0][2][0] = dp[i - 1][0][1][0];
				dp[i][0][2][1] = dp[i - 1][0][1][1];
			}
		}
		else if (c == '_') {
			if (i == 1) {
				dp[1][1][0][0] = 20;
				dp[1][1][0][1] = 1;
				dp[1][0][1][0] = 5;
			}
			else {
				dp[i][1][0][0] = (dp[i - 1][0][1][0] + dp[i - 1][0][2][0]) * 20;
				dp[i][1][0][1] = (dp[i - 1][0][1][0] + dp[i - 1][0][2][0]) + (dp[i - 1][0][1][1] + dp[i - 1][0][2][1]) * 21;
				dp[i][2][0][0] = dp[i - 1][1][0][0] * 20;
				dp[i][2][0][1] = dp[i - 1][1][0][0] + dp[i - 1][1][0][1] * 21;

				dp[i][0][1][0] = (dp[i - 1][1][0][0] + dp[i - 1][2][0][0]) * 5;
				dp[i][0][1][1] = (dp[i - 1][1][0][1] + dp[i - 1][2][0][1]) * 5;
				dp[i][0][2][0] = dp[i - 1][0][1][0] * 5;
				dp[i][0][2][1] = dp[i - 1][0][1][1] * 5;

			}
		}
		else {
			if (i == 1) {
				if (c == 'L') {
					dp[1][1][0][1] = 1;
				}
				else {
					dp[1][1][0][0] = 1;
				}
			}
			else {
				if (c == 'L') {
					dp[i][1][0][1] = dp[i - 1][0][1][0] + dp[i - 1][0][2][0] + dp[i - 1][0][1][1] + dp[i - 1][0][2][1];
					dp[i][2][0][1] = dp[i - 1][1][0][0] + dp[i - 1][1][0][1];
				}
				else {
					dp[i][1][0][0] = dp[i - 1][0][1][0] + dp[i - 1][0][2][0];
					dp[i][2][0][0] = dp[i - 1][1][0][0];
					dp[i][1][0][1] = dp[i - 1][0][1][1] + dp[i - 1][0][2][1];
					dp[i][2][0][1] = dp[i - 1][1][0][1];
				}
			}
		}
	}
	long long ans = dp[s.size()][1][0][1] + dp[s.size()][2][0][1] + dp[s.size()][0][1][1] + dp[s.size()][0][2][1];
	cout << ans << "\n";
}